		<h2>PENDAFTARAN CPNS 2018 BPS DI BUKA MULAI <br>
	26 SEPTEMBER-9 OKTOBER 2018 </h2></center>
<hr size="5" color="black">

<p>Kali ini, BPS akan menerima Calon Pegawai Aparatur Sipil Negara dengan membuka 10 jabatan serta mengalokasikan sebanyak 317 orang, yang terdiri dari 278 orang formasi umum, 32 orang formasi cumlaude, dan 7 orang formasi untuk penyandang disabilitas.</p> 

<p> Penerimaan CPNS 2018 BPS berdasarkan Keputusan Menteri Pendayagunaan Aparatur Negara dan reformasi Birokrasi Nomor 73 tahun 2018 tentang Kebutuhan Pegawai Aparatur Sipil Negara di Lingkungan Badan Pusat Statistik Tahun Anggaran 2018. </p>

<p> Dikutip dari laman BPS, Senin (24/9/2018), berikut ini jabatan, kualifikasi pendidikan, persyaratan umum, persyaratan khusus, jadwal seleksi, serta tata cara pendaftaran CPNS 2018 BPS:<br>
</p>

<b>Jabatan dan Kualifikasi Pendidikan : </b><br>

<ol type="1" >
	
	<li>Penata Laporan Keuangan <br>
	Kualifikasi Pendidikan: D-III Akuntansi</li>

	<li>Apoteker Ahli Pertama <br>
	Kualifikasi Pendidikan: Apoteker</li>

	<li>Asisten Apoteker Terampil <br>
	Kualifikasi Pendidikan: D-III Farmasi</li>

	<li>Dokter Ahli Pertama <br> 
	Kualifikasi Pendidikan: Dokter Umum</li>

	<li>Perawat Gigi Terampil <br> 
	Kualifikasi Pendidikan: D-III Perawat Gigi</li>

</ol>


<b>Persyaratan Umum</b> <br>

<ol type="a">
	
	<li>Warga Negara Indonesia;</li>
	<li>Bertakwa kepada Tuhan Yang Maha Esa;</li>
	<li>Memiliki integritas yang tinggi terhadap Negara Kesatuan Republik Indonesia;</li>
	<li>Tidak pernah dipidana dengan pidana penjara berdasarkan putusan pengadilan yang telah mempunyai kekuatan hukum yang tetap karena melakukan tindak pidana dengan pidana penjara;</li>
	<li>Tidak pernah diberhentikan dengan hormat tidak atas permintaan sendiri atau tidak dengan hormat sebagai PNS, anggota TNI/POLRI, Pegawai BUMN/BUMD atau diberhentikan tidak dengan hormat sebagai pegawai swasta;</li>
	<li>Tidak berkedudukan sebagai Calon Pegawai Negeri Sipil atau Pegawai Negeri Sipil, prajurit TNI, anggota Polri, dan siswa sekolah ikatan dinas Pemerintah;</li>
	<li>Tidak menjadi anggota atau pengurus partai politik atau terlibat politik praktis;</li>
	<li>Memiliki kualifikasi pendidikan sesuai dengan persyaratan jabatan (non kependidikan);</li>
	<li>Sehat jasmani dan rohani sesuai dengan jabatan yang dilamar;</li>
	<li>Tidak memiliki ketergantungan terhadap narkotika dan obat-obatan terlarang atau sejenisnya;</li>
	<li>Bersedia ditempatkan di seluruh wilayah Negara Kesatuan Republik Indonesia;</li>
	<li>Bagi Wanita tidak bertato/bekas tato dan tindik/bekas tindik anggota badan lainnya selain di telinga kecuali yang disebabkan oleh ketentuan agama atau adat; dan bagi Pria tidak bertato/bekas tato dan tindik/bekas tindik seluruh anggota badan kecuali yang disebabkan oleh ketentuan agama atau adat.</li>

</ol>

<b>Persyaratan Khusus</b>
<ol type="a">

	<li>Mempunyai kompetensi yang diperlukan.</li>
	<li>Pada saat mendaftar berusia minimal 19 tahun dan maksimal 34 tahun, kecuali untuk formasi penyandang disabilitas berusia minimal 18 tahun dan maksimal 35 tahun</li>
	<li>Calon pelamar merupakan lulusan Perguruan Tinggi Dalam Negeri dan Program Studi yang terakreditasi dalam Badan Akreditasi Nasional Perguruan Tinggi Negeri (BAN-PT) dan terdaftar di Forlap Kementerian Riset, Teknologi, dan Pendidikan Tinggi saat kelulusan, atau lulusan Perguruan Tinggi Luar Negeri setelah memperoleh penyetaraan ijazah dari Kementerian Riset, Teknologi, dan Pendidikan Tinggi, dengan dengan lndeks Prestasi Kumulatif (IPK) Minimal 2,75.</li>
	<li>Bersedia ditempatkan di wilayah yang telah ditentukan oleh panitia dan tidak dapat pindah tugas sebelum masa kerja mencapai 5 tahun.</li>

</ol>

