<li> Verifikasi Peserta Formasi Penyandang Disabilitas : 11 - 12 Oktober 2018 </li>

<li> Pengumuman Seleksi Administrasi : 16 Oktober 2018 </li>

<li> Cetak Nomor Ujian Secara Online : 16 Oktober 2018 </li>

<li> Seleksi Kompetensi Dasar (CAT) : 23 Oktober- 22 November 2018 </li>

<li> Pengumuman Seleksi Kompetensi Dasar : 23 Oktober- 22 November 2018 </li>

<li> Seleksi Kompetensi Bidang (CAT) : 23 Oktober- 22 November 2018 </li>

<li> Seleksi Kompetensi Bidang Melalui Wawancara : 23 Oktober- 22 November 2018 </li>

<li> Pengumuman Kelulusan Akhir Online : 1 Desember 2018 </li>

<li> Pemberkasan bagi Peserta yang Dinyatakan Lulus pada Pengumuman Kelulusan Akhir : 1 - 28 Desember 2018 </li>

</ol> <br>
