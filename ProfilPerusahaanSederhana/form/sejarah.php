  <h4 class="alert-heading">Sejarah Kapal Api Indonesia : </h4>
  <p>Seperti pun Kopi Aroma, Kopi Kapal Api telah ada sejak zaman Belanda. Didirikan oleh Go Soe Loet bersama dengan kedua saudaranya yaitu Go Bi Tjong dan Go Soe Bin pada tahun 1927. Awalnya mereka mendirikan pabrik penggorengan kopi di kawasan Pabean, Surabaya dengan merek Kopi Bubuk Hap Hoo Tjan. Produk kopi bubuk tersebut lantas dijual keliling kampung dan sekitaran Pelabuhan Tanjung Perak menggunakan sepeda onthel. Pimpinan PT. Santos Jaya Abadi, Soedomo yang merupakan anak dari Go Soe Loet pun saat itu turut membantu memasarkan produk tersebut bersama dua saudaranya yang lain. Yaitu, Indra dan Soetikmo. Para pelanggan kopi mereka, kebanyakan adalah para pelaut yang kebetulan bersandar dan tentu saja orang-orang di sekitar pelabuhan.</p>
  <hr>
  <p>Karena seringnya berjualan di sekitar pelabuhan maka tanpa disengaja muncullah ide untuk membuat semacam lambang perusahaan. Inspirasi lahir dari seringnya menyaksikan kapal api yang lalu-lalang di pelabuhan. Maka diputuskanlah lambang perusahaan adalah Kapal Api. Lambang tersebut mencerminkan penggabungan antara harapan baru, semangat juang, dan tentu saja teknologi. Karena pada masa itu, kapal api dianggap mewah dengan segala kecanggihannya. Kapal api sebagai transportasi laut yang membawa harapan dalam dunia perdagangan.</p>
  <hr>
  <p>Kualitas dan kuantitas yang terjaga, membuat perusahaan ini maju dengan pesat. Hingga akhirnya Goe Soe Loet dan kedua saudaranya pecah kongsi.  Goe Soe Loet mendapatkan bagian pabrik penggorengan kopi dan melanjutkan produksi kopi dengan dibantu oleh anak-anaknya Soetikno, Indra, dan juga Soedomo. Menginjak tahun 1980, perusahaan kopi bubuk Hap Hoo Tjan mengalami penurunan omset penjualan hingga akhirnya gulung tikar. Untungnya Soedomo dan dua saudaranya telah jauh hari membuat perusahaan dengan nama PT. Santos Jaya Abadi. Usai bisnis ayahnya bangkrut, bisnis yang dibuat oleh Soedomo ini semakin meningkat. Jadi memang dari awal, PT. Santos Jaya Abadi bukanlah perusahaan warisan.</p>
  <hr>
  <p>Semakin berjalannya waktu, bisnis yang digarap oleh Soedomo ini semakin maju. Berbekal produk bernama Kapal Api, mulailah sayap-sayap bisnis usahanya berkembang dengan pesat. Kopi tetap menjadi andalan mereka. Keunggulan Kopi Kapal Api terletak pada kualitasnya. Cita rasa kopi yang khas dan cocok dengan lidah orang Indonesia ini membuat Kopi Kapal Api mendapatkan tempat yang baik di hati masyarakat. Kopi Kapal Api baik dalam bungkus sachet maupun bungkus besar adalah hasil resep racikan sendiri dan turun-temurun.</p>
  <hr>
  <p>Pada tahun 1985, Kopi Kapal Api mulai memasuki pasar mancanegara. Mereka mengawalinya dengan menginvasi Arab Saudi. Dan tak berselang lama dari sana, dua tahun kemudian yaitu pada 1987, mereka memasuki pasar Hongkong, kemudian Malaysia dan menyusul Taiwan. Selain kopi, PT. Santos Jaya Abadi juga membuat produk lainnya. Yaitu permen Espresso Candy dan juga permen Relaxa. Kedua permen tersebut menjadi produk andalan mereka. </p>
  <hr>
  <p class="mb-0">Sejarah Kopi Kapal Api dari pelabuhan hingga menguasai pasar dengan segala dinamikanya merupakan tonggak tersebarnya kopi sachet di Indonesia. Harganya yang bersahabat dan kualitasnya yang konsisten membuat produk ini dikenal masyarakat di berbagai lapisan.</p>
